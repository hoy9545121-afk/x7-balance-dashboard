def s(ws, r, c, v):
    """cell set shorthand"""
    ws.cell(row=r, column=c).value = v

def apply_block(ws, rows, cds=None, mps=None, dmgs=None,
                attack_types=None, buff_descs=None,
                dps_formulas=None, k_formulas=None):
    """5-row 스킬 블록에 값 적용
       None 항목은 변경하지 않음"""
    for i, row in enumerate(rows):
        if cds        and cds[i]        is not None: s(ws, row, 4, cds[i])
        if mps        and mps[i]        is not None: s(ws, row, 5, mps[i])
        if dmgs       and dmgs[i]       is not None: s(ws, row, 7, dmgs[i])
        if attack_types and attack_types[i] is not None: s(ws, row, 6, attack_types[i])
        if buff_descs  and buff_descs[i]  is not None: s(ws, row, 8, buff_descs[i])
        if dps_formulas and dps_formulas[i] is not None: s(ws, row, 9, dps_formulas[i])
        if k_formulas  and k_formulas[i]  is not None: s(ws, row, 11, k_formulas[i])

